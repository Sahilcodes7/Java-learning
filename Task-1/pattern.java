import java.lang.*;

public class pattern
{

	public static void main(String args[])          // Right-hand pyramid
	{
		System.out.println("*");
		System.out.println("* *");
		System.out.println("* * *");
		System.out.println("* * * *");
		System.out.println("* * * * *");
	

	      //reverse
	
		System.out.println("\n* * * * *");
		System.out.println("* * * *");
		System.out.println("* * *");
		System.out.println("* *");
		System.out.println("*");
	


			// left-hand

		
		System.out.println("        *");
		System.out.println("      * *");
		System.out.println("    * * *");
		System.out.println("  * * * *");
		System.out.println("* * * * *");

	}
}

